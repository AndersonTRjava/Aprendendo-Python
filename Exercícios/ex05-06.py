# Leia um número e mostre seu sucessor e antecessor
n = int(input("Digite um número: "))
print(f"O número digitado foi: {n} \n Seu sucessor é: {n+1} \n Seu antecessor é: {n-1}")

# Mostrar o dobro, triplo e raiz quadrada do número digitado
import math
print(f" Seu dobro é: {n*2} \n Seu triplo é: {n*3} \n Sua raiz quadrada é: {round(math.sqrt(n),2)}")

